import UIKit

extension UIColor {
    class var main: UIColor {
        UIColor(named: "main")!
    }
    
    class var secondary: UIColor {
        UIColor(named: "secondary")!
    }
    
    class var lineColor: UIColor {
        UIColor(named: "lineColor")!
    }
    
    class var productBackground: UIColor {
        UIColor(named: "productBackground")!
    }
}
